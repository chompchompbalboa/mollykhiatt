
//-----------------------------------------------------------------------------
// Requires
//-----------------------------------------------------------------------------
var React = require('react');
var Radium = require('radium');

var siteActions = require('../../actions/siteActions');

//-----------------------------------------------------------------------------
// Module
//-----------------------------------------------------------------------------
var SiteHeaderShare = React.createClass({
    //---------------------------------------------------------------------------
    // Display Name
    //---------------------------------------------------------------------------

    //---------------------------------------------------------------------------
    // Prop Types
    //---------------------------------------------------------------------------

    //---------------------------------------------------------------------------
    // Mixins
    //---------------------------------------------------------------------------

    //---------------------------------------------------------------------------
    // Statics
    //---------------------------------------------------------------------------

    //---------------------------------------------------------------------------
    // Get Initial State
    //---------------------------------------------------------------------------

    //---------------------------------------------------------------------------
    // Get Default Props
    //---------------------------------------------------------------------------

    //---------------------------------------------------------------------------
    // Component Will Mount
    //---------------------------------------------------------------------------

    //---------------------------------------------------------------------------
    // Component Did Mount
    //---------------------------------------------------------------------------

    //---------------------------------------------------------------------------
    // Component Will Recieve Props
    //---------------------------------------------------------------------------

    //---------------------------------------------------------------------------
    // Should Component Update
    //---------------------------------------------------------------------------

    //---------------------------------------------------------------------------
    // Component Will Update
    //---------------------------------------------------------------------------

    //---------------------------------------------------------------------------
    // Component Did Update
    //---------------------------------------------------------------------------

    //---------------------------------------------------------------------------
    // Component Will Unmount
    //---------------------------------------------------------------------------

    //---------------------------------------------------------------------------
    // Handles
    //---------------------------------------------------------------------------

    handleClick: function(e) {
        e.preventDefault();
        var active = "contact";
        var previous = (this.props.site.private.active === "menu" ? this.props.site.private.previous : this.props.site.private.active);
        var changes = [
            {"key": "private.active", "value": active},
            {"key": "private.load", "value": "link"},
            {"key": "private.previous", "value": previous},
            {"key": "private.url", "value": "contact"},
            {"key": "private.SiteCoverOverlay.opacity", "value": "0.25"}
        ];
        siteActions.changeContent(changes);
    },

    //---------------------------------------------------------------------------
    // Style
    //---------------------------------------------------------------------------

    style: function(color, container) {
        var style = {
            div: {
                width: '30vw',
                padding: '0 3vw 0 0',
                display: 'flex',
                justifyContent: 'flex-end',
                alignItems: 'center',
                '@media (max-width: 48em)': {
                    width: '0'    
                }
            },
            container: {
                textTransform: 'uppercase',
                '@media (max-width: 48em)': {
                    display: 'none'   
                }
            },
            a: {
                color: color,
                width: '100%',
                height: '100%',
                textDecoration: 'none'
            },
        };

        return style;
    },

    //---------------------------------------------------------------------------
    // Render
    //---------------------------------------------------------------------------

    render: function() {

        var {color, site, ...other} = this.props;
        var style = this.style(color, site.private.container);

        return (
            <div id="site-header-share" style={style.div}>
                <div style={style.container}>
                    <a href="/contact" style={style.a} onClick={this.handleClick}>
                        Contact
                    </a>
                </div>
            </div>
        )
    }
    
});
    
//-----------------------------------------------------------------------------
// Export
//-----------------------------------------------------------------------------
module.exports = Radium(SiteHeaderShare);