
//-----------------------------------------------------------------------------
// Requires
//-----------------------------------------------------------------------------
var React = require('react');
var Radium = require('radium');
//-----------------------------------------------------------------------------
// Module
//-----------------------------------------------------------------------------
var AdminDashboardMessage = React.createClass({
    //-------------------------------------------------------------------------
    // Display Name
    //-------------------------------------------------------------------------

    //-------------------------------------------------------------------------
    // Prop Types
    //-------------------------------------------------------------------------

    //-------------------------------------------------------------------------
    // Mixins
    //-------------------------------------------------------------------------

    //-------------------------------------------------------------------------
    // Statics
    //-------------------------------------------------------------------------

    //-------------------------------------------------------------------------
    // Get Initial State
    //-------------------------------------------------------------------------

    //-------------------------------------------------------------------------
    // Get Default Props
    //-------------------------------------------------------------------------

    //-------------------------------------------------------------------------
    // Component Will Mount
    //-------------------------------------------------------------------------

    //-------------------------------------------------------------------------
    // Component Did Mount
    //-------------------------------------------------------------------------

    //-------------------------------------------------------------------------
    // Component Will Recieve Props
    //-------------------------------------------------------------------------

    //-------------------------------------------------------------------------
    // Should Component Update
    //-------------------------------------------------------------------------

    //-------------------------------------------------------------------------
    // Component Will Update
    //-------------------------------------------------------------------------

    //-------------------------------------------------------------------------
    // Component Did Update
    //-------------------------------------------------------------------------

    //-------------------------------------------------------------------------
    // Component Will Unmount
    //-------------------------------------------------------------------------

    //-------------------------------------------------------------------------
    // Message
    //-------------------------------------------------------------------------

    message: function(constant, seed) {
        var message = this[constant](seed);
        return message;
    },

    //-------------------------------------------------------------------------
    // Message List
    //-------------------------------------------------------------------------

    LOGIN_FAILED: function(seed) {
        var message = "Your username or password is incorrect";
        return message;
    },

    LOGOUT_FAILED: function(seed) {
        var message = "There was an error processing your logout request. Please try again.";
        return message;
    },

    LOGOUT_SUCCESS: function(seed) {
        var message = "You have been successfully logged out";
        return message;
    },

    SIGN_IN: function(seed) {
        var message = "You need to sign in to make changes";
        return message;
    },

    WELCOME: function(seed) {
        var message = "Yo. Yo yo.";
        return message;
    },

    //-------------------------------------------------------------------------
    // Handles
    //-------------------------------------------------------------------------

    //-------------------------------------------------------------------------
    // Style
    //-------------------------------------------------------------------------

    style: function() {
        var style = {
            section: {
                margin: '0 0 0 2.5%',
                width: '95%',
                fontWeight: '200',
                fontSize: '18px',
                textAlign: 'center',
                '@media (min-width: 48em) and (max-width: 64em)': {
                    fontSize: '18px'
                },
                '@media (min-width: 64em)': {
                    fontSize: '18px'
                }
            }
        };

        return style;
    },

    //-------------------------------------------------------------------------
    // Render
    //-------------------------------------------------------------------------

    render: function() {
        
        var {admin, seed, ...other} = this.props;
        var message = this.message(admin.public.message, seed);
        var style = this.style();

        return (
        <section id="admin-dashboard-message" style={style.section}>
            {message}
        </section>
        )
    }
    
});
    
//-----------------------------------------------------------------------------
// Export
//-----------------------------------------------------------------------------
module.exports = Radium(AdminDashboardMessage);