<?php
/*
|--------------------------------------------------------------------------
| React Group
|--------------------------------------------------------------------------
*/

Route::group(['middleware' => ['web']], function () {

    /*
    |--------------------------------------------------------------------------
    | App
    |--------------------------------------------------------------------------
    */

    Route::any('/', [
        "as" => "home",
        "uses" => "ReactController@home",
    ]);

    Route::post('/react/{action}/{info?}', [
        "as" => "react",
        "uses" => "ReactController@react",
    ]);

    Route::any('/admin', [
        "as" => "admin",
        "uses" => "AdminController@home",
    ]);

    Route::post('/admin/{action}/{info?}', [
        "as" => "admin_react",
        "uses" => "AdminController@react",
    ]);

    /*
    |--------------------------------------------------------------------------
    | Sites
    |--------------------------------------------------------------------------
    */
    
});
